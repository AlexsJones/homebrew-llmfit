      url "https://github.com/AlexsJones/llmfit/releases/download/v#{version}/llmfit-v#{version}-aarch64-apple-darwin.tar.gz"
      sha256 "57bbd61a0a2c0ac5e75b27d0bf71f7122e227811cd8042ae573b48c0d30d20f1"

      url "https://github.com/AlexsJones/llmfit/releases/download/v#{version}/llmfit-v#{version}-aarch64-unknown-linux-musl.tar.gz"
      sha256 "00b2aa6426df74e9dbfcdaf7bf261ca8730472002612f1c548756abc2704dda5"
